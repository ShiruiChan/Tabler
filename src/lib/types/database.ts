export type BillingPeriod = 'monthly' | 'yearly' | 'one_time';

export interface Module {
  /** Machine key - lowercase snake_case, matches '^[a-z][a-z0-9_]*$'. */
  id: string;
  /** Human-readable display name of the module. */
  name: string;
  /** Optional description shown in the admin module catalog. */
  description: string | null;
  /** Platform base price in cents (0 = included in all plans). */
  base_price_cents: number;
  /** Billing recurrence for this module. */
  billing_period: BillingPeriod;
  /** When false the module is hidden and cannot be enabled for any tenant. */
  is_active: boolean;
  created_at: string;
  updated_at: string;
}

