/**
 * Lifecycle status of an event ticket purchase.
 * Must stay in sync with the event_tickets_status_values CHECK in 0007_events.sql.
 *
 * 'reserved'  - seats held pending payment; capacity is consumed immediately
 *               by the DB check_event_capacity trigger.
 * 'paid'      - payment confirmed (Stripe webhook, TASK-030).
 * 'cancelled' - buyer self-cancelled (reserved only, via visitor RLS + guard
 *               trigger) or staff/owner cancelled.
 * 'refunded'  - payment reversed via Stripe refund flow (TASK-030).
 */
export type EventTicketStatus =
  | 'reserved'
  | 'paid'
  | 'cancelled'
  | 'refunded';

/**
 * A ticketed or free event hosted by a tenant.
 *
 * Named RestaurantEvent to avoid clashing with the DOM global Event type.
 * The underlying SQL table is `public.events`.
 */
export interface RestaurantEvent {
  /** UUID primary key, generated by the database. */
  id: string;
  /** UUID of the owning tenant. */
  tenant_id: string;
  /** Display title of the event (1–160 characters). */
  title: string;
  /**
   * Optional long-form description of the event.
   * Maximum 4000 characters; null until set.
   */
  description: string | null;
  /**
   * Public URL of the event banner/cover image in the tenant-assets bucket.
   * Null until the owner uploads one.
   */
  image_url: string | null;
  /** ISO-8601 timestamptz: when the event starts (UTC). */
  starts_at: string;
  /**
   * ISO-8601 timestamptz: when the event ends (UTC).
   * Null for open-ended events.  When non-null, always later than starts_at
   * (enforced by DB CHECK).
   */
  ends_at: string | null;
  /**
   * Total number of tickets available for this event (1–10 000).
   * Enforced by the check_event_capacity DB trigger: the sum of quantity
   * across all reserved+paid event_tickets rows may never exceed this value.
   */
  capacity: number;
  /**
   * Ticket face value in integer cents (0 = free event, max 10 000 000).
   * This is the current price; historical purchases snapshot the value at
   * purchase time in event_tickets.unit_price_cents.
   */
  price_cents: number;
  /**
   * ISO 4217 currency code for this event's tickets.
   * Consumed by Stripe (TASK-030) as the payment currency.
   */
  currency: Currency;
  /**
   * When false the event is in draft mode and hidden from the public listing.
   * Public SELECT RLS policy requires is_published = true.
   */
  is_published: boolean;
  created_at: string;
  updated_at: string;
}

/**
 * A single ticket purchase batch for an event.
 * One row represents a checkout of `quantity` seats made in a single transaction.
 * This maps cleanly to a single Stripe line item (TASK-030).
 */
export interface EventTicket {
  /** UUID primary key, generated by the database. */
  id: string;
  /**
   * UUID of the owning tenant.
   * Denormalized for RLS performance.
   * Enforced to match event's tenant_id by the check_event_ticket_event_tenant trigger.
   */
  tenant_id: string;
  /** UUID of the parent event (references public.events, CASCADE on delete). */
  event_id: string;
  /**
   * UUID referencing auth.users(id).
   * Null for guest purchases created via the service-role API (TASK-023).
   * SET NULL when the user account is deleted.
   */
  user_id: string | null;
  /**
   * Full name of the buyer (1–120 characters).
   * Required; used for confirmation emails and the B2B attendee dashboard.
   */
  buyer_name: string;
  /**
   * Email address of the buyer.
   * Basic format enforced by DB CHECK (position('@') > 1 AND length 3–254).
   * Null if not provided.
   */
  buyer_email: string | null;
  /**
   * Phone number of the buyer (5–40 characters).
   * Null if not provided.
   */
  buyer_phone: string | null;
  /** Number of seats purchased in this batch (1–100). */
  quantity: number;
  /**
   * Snapshot of events.price_cents at the time of purchase (≥ 0).
   * Immutable after creation - the event price may change later without
   * retroactively altering this record.  Integrity of this snapshot for
   * visitor INSERTs is validated in the TASK-023 API layer; the DB trigger
   * enforces capacity but not price correctness.
   */
  unit_price_cents: number;
  /**
   * Snapshot of events.currency at the time of purchase.
   * Immutable after creation - matches the Stripe charge currency.
   */
  currency: Currency;
  /** Lifecycle status of this ticket purchase. */
  status: EventTicketStatus;
  /**
   * Stripe payment / charge identifier.
   * Null until payment is initiated (TASK-030).
   */
  payment_ref: string | null;
  created_at: string;
  updated_at: string;
}
