/**
 * Discriminant for the three PLAN order flows.
 * Must stay in sync with the orders_order_type_values CHECK in 0009_orders.sql.
 *
 * 'in_session' - dine-in order tied to a physical table (table_id).
 * 'delivery'   - off-premise order with a delivery address and zone.
 * 'banquet'    - pre-order tied to a reservation (deposit via TASK-031).
 */
export type OrderType = 'in_session' | 'delivery' | 'banquet';

/**
 * Lifecycle status of an order.
 * Must stay in sync with the orders_status_values CHECK in 0009_orders.sql.
 *
 * Allowed visitor self-transition (guard trigger enforces): pending → cancelled.
 * Full transition rules (API layer, TASK-029):
 *   pending → confirmed → preparing → ready → completed
 *   ready (delivery) → out_for_delivery → completed
 *   any non-terminal → cancelled
 *   completed → refunded (Stripe webhook, TASK-030)
 */
export type OrderStatus =
  | 'pending'
  | 'confirmed'
  | 'preparing'
  | 'ready'
  | 'out_for_delivery'
  | 'completed'
  | 'cancelled'
  | 'refunded';

/**
 * A customer order in one of three flows: in-session (dine-in), delivery,
 * or banquet pre-order.
 *
 * All money columns are integer cents; total_cents is enforced by DB CHECK
 * to equal subtotal_cents + delivery_fee_cents.
 */
export interface Order {
  /** UUID primary key, generated by the database. */
  id: string;
  /**
   * UUID of the owning tenant.
   * Denormalized for RLS performance.
   */
  tenant_id: string;
  /** Discriminant for the three order flows. */
  order_type: OrderType;
  /** Lifecycle status of the order. */
  status: OrderStatus;
  /**
   * UUID referencing auth.users(id).
   * Null for guest orders created via the service-role API (TASK-029).
   * SET NULL when the user account is deleted.
   */
  user_id: string | null;
  /**
   * Full name of the customer placing the order (1–120 characters).
   * Required even for authenticated users (PII snapshot).
   */
  customer_name: string;
  /**
   * Email address of the customer (max 254 characters).
   * No format check at the DB layer - validated by zod/API.
   * Null if not provided.
   */
  customer_email: string | null;
  /**
   * Phone number of the customer (max 40 characters).
   * Null if not provided.
   */
  customer_phone: string | null;
  /**
   * UUID of the physical floor table (in_session orders).
   * Null when order_type ≠ 'in_session', or when the table has been freed/deleted.
   * SET NULL when the floor_table row is deleted (order history preserved).
   */
  table_id: string | null;
  /**
   * UUID of the delivery zone the order was placed in (delivery orders).
   * Null when order_type ≠ 'delivery', or when the zone has been deleted.
   * SET NULL when the delivery_zone row is deleted (order history preserved).
   */
  delivery_zone_id: string | null;
  /**
   * Delivery address for delivery orders (max 500 characters).
   * Non-null when order_type = 'delivery' (enforced by DB CHECK).
   * Null for in_session / banquet orders.
   */
  delivery_address: string | null;
  /**
   * UUID of the linked reservation (banquet orders).
   * Null when order_type ≠ 'banquet', or when not yet linked, or after deletion.
   * SET NULL when the reservation row is deleted (order history preserved).
   */
  reservation_id: string | null;
  /**
   * Optional free-text notes (special requests, dietary info, banquet instructions).
   * Max 2000 characters; null if not provided.
   */
  notes: string | null;
  /**
   * Sum of (unit_price_cents × quantity) across all order_items.
   * Range: 0–100,000,000 cents.
   */
  subtotal_cents: number;
  /**
   * Snapshot of the resolved delivery fee in integer cents.
   * 0 for in_session and banquet orders.
   * Resolution logic (zone fee_override_cents vs. settings base_fee_cents,
   * free_delivery_over_cents threshold) performed by TASK-029 before insert.
   * Range: 0–1,000,000 cents.
   */
  delivery_fee_cents: number;
  /**
   * Total amount charged: subtotal_cents + delivery_fee_cents.
   * Enforced by DB CHECK (orders_total_equals_subtotal_plus_fee).
   * Range: 0–100,000,000 cents.
   */
  total_cents: number;
  /**
   * ISO 4217 currency code snapshotted from tenant/delivery settings at order time.
   * One of 'usd' | 'eur' | 'gbp' | 'rub'.
   */
  currency: Currency;
  /**
   * Banquet deposit amount in integer cents (TASK-031).
   * 0 = no deposit required.  Range: 0–100,000,000 cents.
   * Default: 0.
   */
  deposit_cents: number;
  /**
   * Stripe payment / charge identifier.
   * Null until payment is initiated (TASK-030).
   * Mirrors event_tickets.payment_ref.
   */
  payment_ref: string | null;
  created_at: string; // ISO-8601 timestamptz
  updated_at: string;
}

/**
 * A single dish line within an order.
 * Dish data (name, price) is snapshotted at order-creation time so that
 * historical records survive menu changes and dish deletions.
 *
 * Items are write-once from the visitor perspective (no visitor UPDATE/DELETE
 * RLS policies).  Staff edits flow through tenant-role policies.
 */
export interface OrderItem {
  /** UUID primary key, generated by the database. */
  id: string;
  /**
   * UUID of the owning tenant.
   * Denormalized for RLS performance.
   * Enforced to match order's tenant_id by the check_order_item_order_tenant trigger.
   */
  tenant_id: string;
  /** UUID of the parent order (references public.orders, CASCADE on delete). */
  order_id: string;
  /**
   * UUID referencing public.dishes(id).
   * Null after the source dish is deleted (ON DELETE SET NULL).
   * The snapshot columns below preserve the historical line data.
   */
  dish_id: string | null;
  /**
   * Snapshot of dishes.name at order creation time (1–160 characters).
   * Preserved even after the dish is deleted or renamed.
   */
  dish_name: string;
  /**
   * Snapshot of dishes.price_cents at order creation time (0–10,000,000 cents).
   * Integrity against live prices validated in TASK-029 API layer.
   */
  unit_price_cents: number;
  /** Number of units of this dish in the order (1–100). */
  quantity: number;
  created_at: string; // ISO-8601 timestamptz
  // No updated_at - items are write-once from the visitor perspective.
}

