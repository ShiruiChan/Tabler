/**
 * ISO 4217 currency codes accepted by the platform.
 * @deprecated Alias of the canonical Currency type.  Use Currency directly.
 */
export type DeliveryCurrency = Currency;

/**
 * One entry in the delivery_settings.schedule jsonb object.
 * Keys "0"–"6" (Sunday–Saturday, JS Date.getDay() convention).
 * All times are UTC.
 */
export interface DeliveryScheduleDay {
  /** Wall-clock opening time in "HH:MM" format (UTC). */
  open: string;
  /** Wall-clock closing time in "HH:MM" format (UTC). */
  close: string;
  /** When true this day accepts no delivery orders regardless of open/close. */
  closed: boolean;
}

/**
 * Shape of delivery_settings.schedule.
 * Keys are weekday numbers as strings ("0" = Sunday … "6" = Saturday).
 * Missing keys are treated as closed days by the application layer.
 * {} is the canonical "not yet configured" sentinel (the DB default).
 */
export type DeliverySchedule = Partial<Record<'0' | '1' | '2' | '3' | '4' | '5' | '6', DeliveryScheduleDay>>;

/**
 * Per-tenant delivery configuration.
 * Singleton - one row per tenant (auto-created by trigger on tenant INSERT).
 * Primary key is a standalone uuid (id), not PK=FK, with a UNIQUE NOT NULL FK
 * on tenant_id.
 */
export interface DeliverySettings {
  /** UUID primary key, generated by the database. */
  id: string;
  /**
   * UUID of the owning tenant.
   * UNIQUE - enforces the one-row-per-tenant singleton.
   * Matches public.tenants(id).
   */
  tenant_id: string;
  /**
   * Master delivery switch.
   * When false no delivery orders are accepted.
   * Default: false.
   */
  is_enabled: boolean;
  /**
   * Minimum basket value in integer cents required for a delivery order.
   * 0 = no minimum.  Range: 0–10,000,000.
   * Default: 0.
   */
  min_order_cents: number;
  /**
   * Flat delivery fee in integer cents when no zone fee_override_cents applies.
   * 0 = free delivery by default.  Range: 0–1,000,000.
   * Default: 0.
   */
  base_fee_cents: number;
  /**
   * Order subtotal threshold in integer cents at or above which delivery is free.
   * null = no free-delivery tier.  Range when non-null: 0–10,000,000.
   */
  free_delivery_over_cents: number | null;
  /**
   * ISO 4217 currency code for all monetary values in this tenant's delivery flow.
   * One of 'usd' | 'eur' | 'gbp' | 'rub'.
   * Default: 'usd'.
   */
  currency: DeliveryCurrency;
  /**
   * Per-weekday delivery window configuration (UTC).
   * Shape: { "0": { open: "HH:MM", close: "HH:MM", closed: bool }, ... }
   * Keys 0–6 correspond to Sunday–Saturday.  Missing keys are treated as closed
   * days.  {} means "not yet configured".
   */
  schedule: DeliverySchedule;
  /**
   * Estimated delivery time in minutes shown to customers at checkout.
   * null = not displayed.  Range when non-null: 5–480.
   */
  estimated_minutes: number | null;
  created_at: string;
  updated_at: string;
}

/**
 * A named delivery zone belonging to a tenant.
 * Multiple rows per tenant.  Each zone optionally carries a geo-polygon and
 * per-zone fee / minimum-order overrides.
 *
 * For TASK-027 (orders): add a nullable delivery_zone_id uuid FK referencing
 * this table with ON DELETE SET NULL to link an order to the zone it was
 * placed in without destroying order history on zone deletion.
 */
export interface DeliveryZone {
  /** UUID primary key, generated by the database. */
  id: string;
  /** UUID of the owning tenant. */
  tenant_id: string;
  /**
   * Human-readable zone name (1–80 characters).
   * UNIQUE per tenant - constraint name: delivery_zones_tenant_id_name_key
   * (used for 23505 duplicate-name error mapping in TASK-028).
   */
  name: string;
  /**
   * Delivery boundary expressed as an array of [longitude, latitude] pairs.
   * Shape: [[lng0, lat0], [lng1, lat1], ...]
   * null = zone has no map boundary (named area without a drawn polygon).
   * When non-null: ≥3 points, each a 2-number array (enforced by DB trigger).
   */
  polygon: [number, number][] | null;
  /**
   * Per-zone delivery fee override in integer cents.
   * null = use delivery_settings.base_fee_cents for orders in this zone.
   * 0 = free delivery for this zone.  Range when non-null: 0–1,000,000.
   */
  fee_override_cents: number | null;
  /**
   * Per-zone minimum order value override in integer cents.
   * null = use delivery_settings.min_order_cents for orders in this zone.
   * Range when non-null: 0–10,000,000.
   */
  min_order_override_cents: number | null;
  /**
   * When false the zone is suspended without deletion.
   * Inactive zones are hidden from the public B2C map (RLS).
   * Default: true.
   */
  is_active: boolean;
  /** Display order when listing zones in the admin dashboard (ascending). */
  sort_order: number;
  created_at: string;
  updated_at: string;
}

