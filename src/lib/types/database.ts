/**
 * Weekly availability rule for a tenant.
 * One row per (tenant_id, weekday) - UNIQUE constraint enforces the simple
 * single-shift model (split shifts are out of scope).
 *
 * weekday follows JavaScript's Date.getDay() convention: 0 = Sunday, 6 = Saturday.
 */
export interface AvailabilityRule {
  /** UUID primary key, generated by the database. */
  id: string;
  /** UUID of the owning tenant. */
  tenant_id: string;
  /**
   * Day of the week: 0 (Sunday) … 6 (Saturday).
   * Matches JavaScript's Date.getDay() convention.
   */
  weekday: number;
  /**
   * Time the venue opens on this weekday, in "HH:MM:SS" format.
   * Always earlier than closes_at (enforced by DB CHECK).
   */
  opens_at: string;
  /**
   * Time the venue closes on this weekday, in "HH:MM:SS" format.
   * Always later than opens_at (enforced by DB CHECK).
   */
  closes_at: string;
  /**
   * Slot granularity in minutes.
   * One of 15, 30, or 60.
   */
  slot_minutes: 15 | 30 | 60;
  /**
   * How many minutes before closes_at the last available booking slot begins.
   * Range: 0–480.  Default: 90.
   */
  last_seating_minutes: number;
  /**
   * When true this weekday is fully closed.
   * opens_at / closes_at are stored but ignored by the availability engine.
   */
  is_closed: boolean;
  created_at: string;
  updated_at: string;
}

